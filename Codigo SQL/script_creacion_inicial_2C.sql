CREATE TABLE [LA_REVANCHA].[USUARIO](
			 [USU_CODIGO][NUMERIC](18,0) IDENTITY (1,1),
			 [USU_USERNAME][VARCHAR](255) UNIQUE,
			 [USU_PASSWORD][VARCHAR](255),
